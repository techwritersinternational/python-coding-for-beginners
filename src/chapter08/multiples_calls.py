def space_travel(destination, speed="light speed", passengers=2):
    print(f"Traveling to {destination} at {speed} with {passengers} passengers.")

space_travel("Alpha Centauri")
space_travel("Andromeda", "warp speed")
space_travel("Milky Way", passengers=5)
space_travel(speed="ludicrous speed", destination="Galaxy's Edge", passengers=42)