# Define the original tuple of ingredients
ingredients = ('onions', 'garlic', 'ginger', 'tomatoes', 'spices')

# Print the original menu
print("Original ingredients:")
for item in ingredients:
    print(item)

# Redefine the tuple with a new set of ingredients
ingredients = ('bell peppers', 'garlic', 'ginger', 'coconut milk', 'spices')

# Print the new menu
print("\nNew ingredients:")
for item in ingredients:
    print(item)